function slugify(v) {
  return String(v || '')
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
    .toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
}

function ym(dateStr) {
  return dateStr ? String(dateStr).slice(0, 7) : '';
}

function fmt(v) {
  return `R$ ${Number(v || 0).toLocaleString('pt-BR', { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
}

function fmtInt(v) {
  return Number(v || 0).toLocaleString('pt-BR');
}

function fmtDate(dateStr) {
  if (!dateStr) return '—';
  const s = String(dateStr);
  if (s.length >= 10 && s[4] === '-' && s[7] === '-') {
    const [y, m, d] = s.slice(0, 10).split('-');
    return `${d}/${m}/${y}`;
  }
  if (s.length >= 7 && s[4] === '-') {
    const [y, m] = s.slice(0, 7).split('-');
    return `01/${m}/${y}`;
  }
  return s;
}

function monthLabel(key) {
  if (!key || key.length < 7) return key || '';
  const [y, m] = key.split('-');
  return `${MONTH_NAMES[m]}/${y}`;
}

function monthShort(key) {
  if (!key || key.length < 7) return key || '';
  const [y, m] = key.split('-');
  return `${MONTH_SHORT[m]}/${y.slice(2)}`;
}

function deriveIdeal(plano, frequencia, fallback) {
  const k = `${plano}||${frequencia}`;
  return IDEAL_BY_COMBO[k] ?? Number(fallback || 0);
}

function getSweet(contract, idealFallback) {
  const month = ym(contract?.fimContrato);
  const k = `${contract?.plano}||${contract?.frequencia}`;
  if (month && BEST_SWEETS[month] && BEST_SWEETS[month][k] != null) return Number(BEST_SWEETS[month][k]);
  return deriveIdeal(contract?.plano, contract?.frequencia, idealFallback);
}

