function bindEvents() {
  document.querySelectorAll('[data-tab]').forEach(btn => {
    btn.addEventListener('click', () => {
      state.tab = btn.dataset.tab;
      render();
    });
  });
  const competenciaSelect = document.getElementById('competenciaSelect');
  if (competenciaSelect) {
    competenciaSelect.addEventListener('change', (e) => {
      state.competencia = e.target.value;
      render();
    });
  }
  const precosMesSelect = document.getElementById('precosMesSelect');
  if (precosMesSelect) {
    precosMesSelect.addEventListener('change', (e) => {
      state.precosMes = e.target.value;
      render();
    });
  }
  document.querySelectorAll('[data-competencia-chip]').forEach((el) => {
    el.addEventListener('click', () => {
      state.competencia = el.getAttribute('data-competencia-chip');
      render();
    });
  });
  document.querySelectorAll('[data-hstatus]').forEach((el) => {
    el.addEventListener('click', () => {
      state.historicoStatus = el.getAttribute('data-hstatus');
      render();
    });
  });
  document.querySelectorAll('[data-hsweet]').forEach((el) => {
    el.addEventListener('click', () => {
      state.historicoSweet = el.getAttribute('data-hsweet');
      render();
    });
  });
  const manualForm = document.getElementById('manualForm');
  if (manualForm) {
    manualForm.addEventListener('submit', (e) => {
      e.preventDefault();
      const fd = new FormData(manualForm);
      applyManualEntry(Object.fromEntries(fd.entries()));
    });
  }
  const exportBtn = document.getElementById('exportBtn');
  if (exportBtn) exportBtn.addEventListener('click', exportPatients);
  const resetBtn = document.getElementById('resetBtn');
  if (resetBtn) resetBtn.addEventListener('click', resetPatients);
}

document.addEventListener('DOMContentLoaded', render);
