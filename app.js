function competenciesFromPatients(patients) {
  const set = new Set();
  patients.forEach(p => {
    if (p.contratoAtual?.fimContrato) set.add(ym(p.contratoAtual.fimContrato));
    if (p.contratoAnterior?.fimContrato) set.add(ym(p.contratoAnterior.fimContrato));
    (p.historicoRenovacoes || []).forEach(h => h.competencia && set.add(h.competencia));
  });
  return Array.from(set).filter(Boolean).sort();
}

function activePatients() {
  return state.patients.filter(p => p.ativo !== false);
}

function currentMetrics() {
  const active = activePatients();
  const receitaAtual = active.reduce((s, p) => s + Number(p.contratoAtual.valor || 0), 0);
  const atendimentosMes = active.reduce((s, p) => s + (AULAS_MES[p.contratoAtual.frequencia] || 0), 0);
  const valorMedioAtendimento = atendimentosMes ? receitaAtual / atendimentosMes : 0;
  const prejuizo = active.reduce((s, p) => s + Math.max(0, deriveIdeal(p.contratoAtual.plano, p.contratoAtual.frequencia, p.valorIdealAtual) - Number(p.contratoAtual.valor || 0)), 0);
  const sweet = active.reduce((s, p) => {
    const ideal = deriveIdeal(p.contratoAtual.plano, p.contratoAtual.frequencia, p.valorIdealAtual);
    return s + Math.max(0, Math.min(getSweet(p.contratoAtual, ideal) - Number(p.contratoAtual.valor || 0), ideal - Number(p.contratoAtual.valor || 0)));
  }, 0);
  return { active, receitaAtual, atendimentosMes, valorMedioAtendimento, prejuizo, sweet };
}

